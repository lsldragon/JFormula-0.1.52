    @Override
    public void drop(DropTargetDropEvent dtde) {
        try {

            dtde.acceptDrop(2);
            String inFileName = "";

            List<File> files = (List<File>) dtde.getTransferable().getTransferData(DataFlavor.javaFileListFlavor);
            File inFile = files.get(0);
            inFileName = inFile.getAbsolutePath();

            if (inFileName.endsWith(".jft") || inFileName.endsWith(".txt")) {
                ta_formula.setText(FileUtil.readFile(inFileName));
            } else {

                new Thread(new Runnable() {
                    @Override
                    public void run() {
                        try {
                            new DisplaySVG(inFile);
                        } catch (Exception ex) {
                            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }, "svgdisplay").start();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(getRootPane(), "Error: " + e.getMessage(), "FATAL ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }

