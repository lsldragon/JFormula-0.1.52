    /*
    Creat Formulas and Settings directories and debuglog.info, config.properties files
     */
    private void CreatDirs() throws IOException {

        File extendFormula = new File("extendFormula");
        if (!extendFormula.exists()) {
            extendFormula.mkdir();
        }

        File cache = new File(".cache");
        if (!cache.exists()) {
            cache.mkdir();
        }

        File settingsDir = new File(ConstantsUI.USER_HOME + File.separator + ".JFormula");
        if (!settingsDir.exists()) {
            settingsDir.mkdir();
        }

        File debugFile = new File(ConstantsUI.USER_HOME + File.separator + ".JFormula" + File.separator + ".Info.info");
        if (!debugFile.exists()) {
            debugFile.createNewFile();

            BufferedWriter bw = new BufferedWriter(new FileWriter(ConstantsUI.USER_HOME + File.separator + ".JFormula" + File.separator + ".Info.info"));
            String[] key = {"os.name", "os.arch", "os.version", "user.name", "file.encoding", "user.language", "user.home",
                "user.dir", "java.home"};
            for (int i = 0; i < key.length; i++) {
                String string = System.getProperty(key[i]);
                bw.write(key[i] + "=" + string);
                bw.newLine();
            }
            bw.write("Screen Size=" + setLocationCenter());
            bw.flush();
            bw.close();
        }
    }

    /*
    make the wondow in the center   haven't call this method
     */
    private String setLocationCenter() {

        Toolkit tk = Toolkit.getDefaultToolkit();
        Dimension dm = tk.getScreenSize();
        this.setLocation((int) (dm.getWidth() / 2) - (this.getWidth() / 2), (int) (dm.getHeight() / 2) - (this.getHeight() / 2));
        return dm.getWidth() + "*" + dm.getHeight();
    }

    private void setMainFrame() {

        Dimension resolution = Toolkit.getDefaultToolkit().getScreenSize();
        int showWidth = (int) (resolution.getWidth() * 0.8);
        int showHeight = (int) (resolution.getHeight() * 0.8);
        getContentPane().setPreferredSize(new Dimension(showWidth, showHeight));
        pack();
        int x = (int) ((resolution.getWidth() - getWidth()) / 2);
        int y = (int) ((resolution.getHeight() - getHeight()) / 2);
        setLocation(x, y);
    }

